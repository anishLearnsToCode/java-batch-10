package day1;

public class DataTypes {
    public static void main(String[] args) {
        // Time complexity : O(1) -- constant time complexity
        // Integers
        byte b; // 1 byte (-128 - 127)
        short s; // 2 byte (-2^15 - 2^15)
        int a; // 4 byte
        long l; // 8 bytes

        // Floating point types
        float f; // low precision (4 bytes)
        double precision; // high precision (8 bytes)

        // Boolean
        boolean var = true;
        boolean x = false;

        // Characters and strings
        char c = 'a'; // 2 bytes - UNICODE

        // String is a Class and name is an object
        String name = "anish"; // !!!NOT A DATATYPE!!!
        name = null;

        // this is not allowed - is an error
        // c = null;

        // single line comment
        /*
        ,mumfnsd
        dfsdfsd
        sdfsdf

        this is multiple line comment
        */
    }
}
